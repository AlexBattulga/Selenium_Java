
public class JavaDataTypes {

	public static void main(String[] args) {
		// Primitive Data Types
		byte b1 = 120;
		short s1 = 32000;
		char c1 = 'A';
		int i1 = 2000; // 2 billions
		long l1 = 289798790; // more than 2 billions
		float f1 = 12.99f;
		double d1 = 20.33;
		boolean b = true; 
		// String is Reference Data Type
		String str = "String";
	}

}
